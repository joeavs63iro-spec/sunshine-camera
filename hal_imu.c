#include "icm42688.h"

//===================================================================================================================
// 系统 HAL 统一对接接口 (对外层暴露，接口名与参数严格不变)
//===================================================================================================================

//-------------------------------------------------------------------------------------------------------------------
//  @brief      初始化 IMU 硬件模块
//-------------------------------------------------------------------------------------------------------------------
void HAL_IMU_Init(void) {
    // 内部实际调用 42688 的初始化流程
    Init_ICM42688(); 
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      获取处理后的 Z 轴角速度数据 (dps)
//-------------------------------------------------------------------------------------------------------------------
float HAL_IMU_GetGyroZ(void) {
    // 触发更新 42688 的陀螺仪数据缓冲
    Get_Gyro_ICM42688(); 
    
    // 从 42688 的全局结构体中抓取 Z 轴角速度数据
    float gz = gICM42688Data.gyro.z;
    
    // 软件消抖 (Deadband Filter) 继承原 45686 的逻辑
    // 若转速微小(处于系统本底噪声中)，强制视作0，防止系统长时间静止后航向角积分偏移
    if(gz > -0.5f && gz < 0.5f) {
        gz = 0.0f;
    }
    
    return gz; 
}